//Take three numbers as input and print the largest of the three.
public class exa_7 {
    public static void main(String[] args)
    {
        int a=40,b=20,c=30;
        if(a>b && a>c)
            System.out.println("largest number a=:"+a);
        else if(b>a && b>c)
            System.out.println("largest number b=:"+b);
        else
            System.out.println("largest number c=:"+c);
    }
}
/*
output :
largest number a=:40
 */