public class logical_operator {
    public static void main(String[] args)
    {
        boolean a = true;
        boolean b = false;
        System.out.println(!b);
        System.out.println(!a);
        System.out.println(a || b);
        System.out.println(a && b);
    }
}
/*
output:

true
false
true
false

 */