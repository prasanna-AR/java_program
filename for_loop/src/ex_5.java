// Calculate the product of numbers from 1 to 10.
public class ex_5 {
    public static void main(String[] args)
    {
       int product =1;
       for(int i=1;i<=10;i++)
       {
           product = product*i;
       }
       System.out.println("Calculate the product of numbers from 1 to 10: "+product);
    }
}
/*
output :
Calculate the product of numbers from 1 to 10: 3628800
 */
