//Sum of Numbers: Find the sum of all numbers from 1 to 50.
public class ex_2 {
    public static void main(String[] args)
    {
        int sum=0;
        for(int i=1;i<=50;i++)
        {
            sum=sum+i;
        }
        System.out.println("sum of all number 1 to 50 : "+sum);
    }
}
/*
output :
sum of all number 1 to 50 : 1275
*/